#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	nb;
	size_t	sign;

	sign = 1;
	nb = 0;
	while ((9 <= *str && *str <= 13) || *str == ' ')
		str++;
	if (*str == '-')
		sign *= -1;
	if (*str == '-' || *str == '+')
		str++;
	while ('0' <= *str && *str <= '9')
	{
		nb = nb * 10 + (*str - '0');
		str++;
	}
	return ((int)(sign * nb));
}
